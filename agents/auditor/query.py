"""Сборка текста аудит-запроса из плана — детерминированная, без мозгов. Спека: 55.

Вектор запроса аудитора считается из плана (spec/55: «план → вектор → поиск по incidents»): здесь
только собирается текст под эмбеддер. Это код, не эвристика над смыслом — план уже составил
executor (LLM), мы лишь склеиваем его шаги в строку. Порядок шагов сохранён, пустые отброшены — тот
же план даёт тот же текст. Юнит-тестируемо без LLM и Qdrant.
"""


def build_query(plan: list[str]) -> str:
    """Шаги плана (в исходном порядке, без пустых) → строка для эмбеддера.

    План ведёт запрос: инциденты ищутся по тому, что система собирается делать. Порядок и отбор
    пустых — детерминированы: сборка из типизированного списка, а не разбор сырого текста.
    """
    return " ".join(step.strip() for step in plan if step.strip())
